package entity;

public class Teacher {

    String teacherno;
    String teachername;
    String teacherkey;
    String teacherjob;
    String jobname;
    String teachergrade;
    String teachersub;

    public Teacher() {
    }

    public String getTeacherno() {
        return teacherno;
    }

    public String getTeachername() {
        return teachername;
    }

    public String getTeacherkey() {
        return teacherkey;
    }

    public String getTeacherjob() {
        return teacherjob;
    }

    public String getJobname() {
        return jobname;
    }

    public String getTeachergrade() {
        return teachergrade;
    }

    public String getTeachersub() {
        return teachersub;
    }

    public void setTeacherno(String teacherno) {
        this.teacherno = teacherno;
    }

    public void setTeachername(String teachername) {
        this.teachername = teachername;
    }

    public void setTeacherkey(String teacherkey) {

        this.teacherkey = teacherkey;
    }

    public void setTeacherjob(String teacherjob) {
        this.teacherjob = teacherjob;
    }

    public void setJobname(String jobname) {
        this.jobname = jobname;
    }

    public void setTeachergrade(String teachergrade) {
        this.teachergrade = teachergrade;
    }

    public void setTeachersub(String teachersub) {
        this.teachersub = teachersub;
    }
}
