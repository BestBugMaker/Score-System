package entity;

public class LargeDetail {
    private String ExamPaperNo;
    private String LargeNo;
    private String LargeName;
    private String SmallCount;
    private String LargeTotalScore;

    public LargeDetail() {
    }

    public String getExamPaperNo() {
        return ExamPaperNo;
    }

    public void setExamPaperNo(String examPaperNo) {
        ExamPaperNo = examPaperNo;
    }

    public String getLargeNo() {
        return LargeNo;
    }

    public void setLargeNo(String largeNo) {
        LargeNo = largeNo;
    }

    public String getLargeName() {
        return LargeName;
    }

    public void setLargeName(String largeName) {
        LargeName = largeName;
    }

    public String getSmallCount() {
        return SmallCount;
    }

    public void setSmallCount(String smallCount) {
        SmallCount = smallCount;
    }

    public String getLargeTotalScore() {
        return LargeTotalScore;
    }

    public void setLargeTotalScore(String largeTotalScore) {
        LargeTotalScore = largeTotalScore;
    }

}
