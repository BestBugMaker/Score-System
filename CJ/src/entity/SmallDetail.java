package entity;

public class SmallDetail {
    private String ExamPaperNo;
    private String LargeNo;
    private String SmallNo;
    private String SmallTotalScore;

    public SmallDetail() {
    }

    public String getExamPaperNo() {
        return ExamPaperNo;
    }

    public void setExamPaperNo(String examPaperNo) {
        ExamPaperNo = examPaperNo;
    }

    public String getLargeNo() {
        return LargeNo;
    }

    public void setLargeNo(String largeNo) {
        LargeNo = largeNo;
    }

    public String getSmallNo() {
        return SmallNo;
    }

    public void setSmallNo(String smallNo) {
        SmallNo = smallNo;
    }

    public String getSmallTotalScore() {
        return SmallTotalScore;
    }

    public void setSmallTotalScore(String smallTotalScore) {
        SmallTotalScore = smallTotalScore;
    }
}
